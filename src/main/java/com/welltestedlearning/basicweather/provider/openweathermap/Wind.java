package com.welltestedlearning.basicweather.provider.openweathermap;

public class Wind {

    private long deg;
    private double speed;

    public long getDeg() {
        return deg;
    }

    public void setDeg(long deg) {
        this.deg = deg;
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }
}
